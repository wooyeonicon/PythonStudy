# os.path操作目录
import os.path
os.path.abspath("")  # 获取文件或目录的绝对路径
os.path.exists('')  # 判断文件或目录是否存在
os.path.join()   # 将目录与目录或文件名拼接起来
os.path.splitext()  # 分离文件名和扩展名
os.path.basename()   # 从一个目录中提取文件名
os.path.dirname()  # 从一个路径中提取文件路径，不包括文件名
os.path.isdir()   # 用于判断是否为路径
